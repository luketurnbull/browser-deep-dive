/**
 * Represents a 2D vector
 */
export class Vector2 {
  /**
   * Creates a 2D vector.
   * @param {number} x - The x-coordinate.
   * @param {number} y - The y-coordinate.
   */
  constructor(x, y) {
    /** @type {number} */
    this.x = x;

    /** @type {number} */
    this.y = y;
  }
}
